export class PadConfiguration{
  /**
   * The key of the pad.
   */
  public key: string;
  
  /**
   * The ke of the image of the pad.
   */
  public imageKey: string;

  /**
   * The scale of the pad in the X axis.
   */
  public scaleX: number;

  /**
   * The scale of the pad in the Y axis.
   */
  public scaleY: number;

  /**
   * The type of the pad.
   */
  public type: String;
}